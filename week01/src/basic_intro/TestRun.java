package basic_intro;

public class TestRun {
    public static void main(String[] args) {
        int studentGrade = 90;
        /*
         * Using Ternary Operator :-)
         */
        System.out.println(studentGrade >= 60 ? "Passed" : "Failed");

        int product = 3;
        while (product <= 100)
            product = 3 * product;

         /*
         * In VScode, just type:
         * > sout then TAB
         * Print to standard out
         * System.out.println();
         * 
         * in Eclipse, type:
         * > sout and press Ctrl + space.
         */
    } // end main()
}
